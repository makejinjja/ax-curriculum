# main.py

def get_user_keywords():
    """
    사용자로부터 관심 키워드를 입력받아 리스트 형태로 반환하는 역할을 합니다.
    이 함수는 순수하게 '입력과 정제' 역할만 담당하여, 추후 유지보수를 쉽게 합니다.
    """
    print("=== 🗞️ 맞춤형 뉴스 브리핑 시스템에 오신 것을 환영합니다 ===")
    
    # 1. 쉼표를 기준으로 여러 키워드를 입력받습니다.
    raw_input = input("관심 있는 뉴스 키워드를 입력하세요 (쉼표로 구분, 예: AI, 경제, 테크): ")
    
    # 사용자가 아무것도 입력하지 않고 엔터만 눌렀을 경우를 위한 방어 코드입니다.
    if not raw_input.strip():
        print("입력된 키워드가 없습니다. 기본값인 'IT' 뉴스로 설정합니다.")
        return ['IT']

    # 2. 쉼표(,)를 기준으로 문자열을 자르고, 각 키워드 앞뒤의 여백을 제거합니다.
    # 예: " AI , 경제 " -> ["AI", "경제"]
    keyword_list = [keyword.strip() for keyword in raw_input.split(',')]
    
    return keyword_list

def main():
    """
    프로그램의 메인 흐름을 담당합니다.
    기능 조각(함수)들을 조립하여 올바른 순서로 실행시킵니다.
    """
    # 관심사 입력받기
    keywords = get_user_keywords()
    
    # 3. 설정된 내용 확인 (추후 이 뒷부분에 뉴스 수집 기능이 연결될 예정입니다)
    print(f"\n[시스템] 설정된 키워드 리스트: {keywords}")
    print("[시스템] 위 키워드를 바탕으로 뉴스 데이터를 가져올 준비를 마쳤습니다.")

# 해당 스크립트가 다른 곳에서 임포트(import)되지 않고, 직접 실행될 때만 main()을 실행시키는 표준 작성법입니다.
if __name__ == "__main__":
    main()
